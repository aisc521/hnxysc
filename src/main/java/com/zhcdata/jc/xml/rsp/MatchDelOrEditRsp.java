package com.zhcdata.jc.xml.rsp;


import com.thoughtworks.xstream.annotations.XStreamAlias;
import lombok.Getter;
import lombok.Setter;
import lombok.ToString;

import java.util.Objects;

@Getter
@Setter
@ToString
@XStreamAlias("match")
public class MatchDelOrEditRsp {
    private String ID;//比赛id
    private String type;//操作类型，删除还是修改
    private String matchtime;//新的时间
    private String oprTime;//操作时间

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        MatchDelOrEditRsp that = (MatchDelOrEditRsp) o;
        return Objects.equals(ID, that.ID);
    }

    @Override
    public int hashCode() {
        return Objects.hash(ID);
    }
}